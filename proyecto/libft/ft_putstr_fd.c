#include "libft.h"

size_t	ft_putstr_fd(char *s, int fd)
{
	int		i;

	i = -1;
	while (s[++i] != '\0')
	{
		if (ft_putchar_fd(s[i], fd) != 0)
			return (-1);
	}
	return (0);
}
