#include "ft_printf.h"

int	print_s(char *s)
{
	if (!s)
	{
		if (ft_putstr_fd("(null)", 1) != 0)
			return (-1);
		return (6);
	}
	if (ft_putstr_fd(s, 1) != 0)
		return (-1);
	return (ft_strlen(s));
}
